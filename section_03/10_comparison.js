// 1. Normal comparison (==) | Compare only value
console.log(3 == 3)
console.log(3 == '3')
console.log('Matheus' == 'Matheus')

// Strict comparison (===) | Compare data type and value
console.log(3 === 3)
console.log(3 === '3')
console.log('Matheus' === 'Matheus')

